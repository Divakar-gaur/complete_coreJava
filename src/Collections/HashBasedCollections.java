package Collections;

public class HashBasedCollections {
    public static void main(String[] args) {
        String s1="krishna";
        String s2="radha";
        String s3="sita";
        String s4="ram";

        System.out.println(s1.hashCode());
        System.out.println(s2.hashCode());
        System.out.println(s3.hashCode());
        System.out.println(s4.hashCode());
    }
}
