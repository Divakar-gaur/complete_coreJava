package Strings;

public class AddingNumbers {
    public static void main(String[] args) {
        System.out.println('1'+'1');  // adding ASCII values :- 98
        System.out.println('2'+'2');  // adding ASCII values :- 100

        //Acual Addition

        System.out.println('1'-'0'+'1'-'0');
        System.out.println('2'-'0'+'2'-'0');
    }
}
